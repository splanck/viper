struct CaEntry
{
    const char *name;
    const u8 *pubkey;
    size_t len;
};

/**
 * @brief List of embedded root CA public keys included in the output bundle.
 *
 * @details
 * Each entry provides a human-readable name (for tool output) and a pointer to
 * the DER-encoded SPKI bytes. The `len` value must match the byte length of
 * the DER blob.
 */
