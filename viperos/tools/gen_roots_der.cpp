/**
 * @file gen_roots_der.cpp
 * @brief Generate a `roots.der` bundle from embedded root CA public keys.
 *
 * @details
 * The kernel TLS stack uses a small built-in trust store for certificate
 * validation. Rather than embedding a full certificate store in the kernel,
 * ViperOS can ship a compact "roots bundle" file on disk.
 *
 * This tool builds that bundle from hard-coded DER blobs and writes it in a
 * simple length-prefixed format:
 *
 * - `u32 count`
 * - For each entry:
 *   - `u32 der_len`
 *   - `der_len` bytes of DER data
 *
 * Each DER blob in this file is expected to be a DER-encoded
 * SubjectPublicKeyInfo (SPKI) for a trusted root CA. The kernel verifier can
 * then compare/validate peer certificate chains against this set of keys.
 */

