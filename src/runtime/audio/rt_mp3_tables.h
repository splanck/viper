//===----------------------------------------------------------------------===//
// Huffman Tables (ISO 11172-3 Annex B, Tables B.6/B.7)
//
// Each pair table entry: {x, y} decoded values. Stored as binary tree nodes.
// Node format: if value >= 0, it's a leaf with x=value>>4, y=value&0xF.
//              if value < 0, it's a branch: left child at -value, right at -value+1.
// Table 0 is empty (all zeros). Tables 4,14 are unused in the spec.
//
// For simplicity, we use a direct {max_val, linbits} per table approach and
// decode using a sequential code-length based method.
//===----------------------------------------------------------------------===//

/// @brief Huffman table properties: {max_xy_value, linbits_extension}
/// Tables 0-31. Table indices map to table_select values from side info.
/// linbits: additional bits appended to values >= 15.
typedef struct {
    int linbits;
    int max_val; // max x or y value without linbits
} mp3_huff_table_info_t;

