std::string shellSingleQuote(std::string_view value) {
    std::string out;
    out.reserve(value.size() + 2);
    out.push_back('\'');
    for (char c : value) {
        if (c == '\'')
            out += "'\\''";
        else
            out.push_back(c);
    }
    out.push_back('\'');
    return out;
}

void validateStubToken(const std::string &value, const char *fieldName) {
    validateSingleLineField(value, fieldName);
    const std::string clean = sanitizePackageRelativePath(value, fieldName);
    if (clean != value)
        throw std::runtime_error(std::string(fieldName) + " was not normalized: " + value);
}

/// @brief Validate an optional payload SHA-256 digest embedded in the stub.
/// @details Empty strings are accepted for standalone stub tests. Complete
///          Bundles generated by buildLinuxAppImage() always provide a
///          lowercase 64-character digest of the appended gzip payload.
/// @param value Digest text to validate.
/// @throws std::runtime_error when @p value is non-empty and not SHA-256 hex.
void validateOptionalSha256(const std::string &value) {
    if (value.empty())
        return;
    if (value.size() != 64)
        throw std::runtime_error("Linux bundle payload SHA-256 must be 64 hex characters");
    for (char c : value) {
        const bool digit = c >= '0' && c <= '9';
        const bool lower = c >= 'a' && c <= 'f';
        if (!digit && !lower)
            throw std::runtime_error("Linux bundle payload SHA-256 must be lowercase hexadecimal");
    }
}

std::vector<uint8_t>::const_iterator payloadMarkerIt(const std::vector<uint8_t> &data) {
    const std::string marker = std::string(kLinuxRuntimePayloadMarker) + "\n";
    return std::search(data.begin(),
                       data.end(),
                       reinterpret_cast<const uint8_t *>(marker.data()),
                       reinterpret_cast<const uint8_t *>(marker.data()) + marker.size());
}

/// @brief Extract a single shell assignment value from the generated stub.
/// @details The runtime stub writes simple single-quoted assignments before
///          the payload marker. This helper is intentionally narrow and is used
///          only by verifyLinuxAppImage() to read payload_sha256.
/// @param text Stub text before the binary payload.
/// @param name Assignment name including no trailing equals sign.
/// @return The unquoted assignment value, or an empty string when absent.
std::string shellAssignmentValue(std::string_view text, std::string_view name) {
    const std::string needle = std::string(name) + "='";
    const size_t start = text.find(needle);
    if (start == std::string_view::npos)
        return {};
    size_t pos = start + needle.size();
    std::string out;
    while (pos < text.size()) {
        char c = text[pos++];
        if (c == '\'') {
            if (pos + 2 < text.size() && text[pos] == '\\' && text[pos + 1] == '\'' &&
                text[pos + 2] == '\'') {
                out.push_back('\'');
                pos += 3;
                continue;
            }
            return out;
        }
        out.push_back(c);
    }
    return {};
}

